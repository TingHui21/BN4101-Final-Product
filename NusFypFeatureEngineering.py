def addNewFeature(dataset, new_feature_list):
    for new_feature in new_feature_list:
        temp_features = new_feature.split(" - ")
        new_col = dataset[temp_features[0]] - dataset[temp_features[1]]
        dataset[new_feature] = new_col

    return dataset


